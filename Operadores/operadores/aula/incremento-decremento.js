//Os operadores de incremento ++ e decremento --, são usados para adicionar ou subtrair o valor 1 de uma variável.

var contador = 5;
contador++;
console.log(contador);// contador agora vale 6

contador--;
console.log(contador);// contador agora vale 5

console.log("----------------");

console.log(--contador);//operador tiver antes da varial faz a atribuição antes de mostra a variavel

console.log("----------------");

var numero_1 = 5;
console.log(numero_1++);//adiciona depois de chamar retorno 5

console.log("----------------");

var numero_2 = 5;
console.log(++numero_2);//adiciona antes de chamar retorno 6