/* Metodo ToFixed é pra arredonda os valores */

var n1 = 12345.89;
console.log(n1.toFixed())
/* arredonda o ultimo valor no caso o 5 com os numeros decimais*/

var n2 = 12344.3371;
console.log(n2.toFixed(2))
/* a diferença é que ta arredondando as casas depois do ponto em duas casas só*/
/* se tiver 10 casas depois do ponto usando o toFixed(2) vai colocar só duas */

